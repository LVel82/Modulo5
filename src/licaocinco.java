public class licaocinco {
    public static void main(String[] args) {

        String nomeCompleto = "Luana Veloso";

        Character nomeAbreviado = 'L';
        Character sobrenomeAbreviado = 'V';

        Integer idade21 = 33;
        Integer idade22 = idade21 + 1;

        Integer altura1 = 156;
        Double altura2 = 1.56d;

        System.out.println("Nome Completo: " + nomeCompleto);
        System.out.println("Abreviação do nome: " + nomeAbreviado);
        System.out.println("Abreviação do sobrenome: " +sobrenomeAbreviado);
        System.out.println("Idade em 2021: " + idade21);
        System.out.println("Idade em 2022: " + idade22);
        System.out.println("Altura em cm: " + altura1);
        System.out.println("Altura em m: " + altura2);
    }
}
